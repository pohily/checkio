class Warrior:
    def __init__(self):
        self.health = 50
        self.attack = 5

    def self_damage(self, enemy):
        self.health -= enemy.attack
        if isinstance(enemy, Vampire):
            enemy.health += enemy.attack * enemy.vampirism
    @property
    def is_alive(self):
        return self.health > 0

class Knight(Warrior):
    def __init__(self):
        Warrior.__init__(self)
        self.attack = 7

class Defender(Warrior):
    def __init__(self):
        Warrior.__init__(self)
        self.health = 60
        self.attack = 3
        self.defense = 2

    def self_damage(self, enemy):
        if enemy.attack > self.defense:
            self.health -= enemy.attack - self.defense
            if isinstance(enemy, Vampire):
                enemy.health += (enemy.attack - self.defense) * enemy.vampirism

class Vampire(Warrior):
    def __init__(self):
        Warrior.__init__(self)
        self.health = 40
        self.attack = 4
        self.vampirism = 0.5

class Lancer(Warrior):
    def __init__(self):
        Warrior.__init__(self)
        self.health = 50
        self.attack = 6
        

        
        
def fight(unit_1, unit_2):
    while unit_1.is_alive and unit_2.is_alive:
        unit_2.self_damage(unit_1)
        if unit_2.is_alive:
            unit_1.self_damage(unit_2)
            
            
    return unit_1.is_alive

class Army():
    def __init__(self):
        self.army = []

    def add_units(self, unit, num):
        self.army += [unit() for i in range(num)]
        
class Battle():
    def fight(self, army1, army2):
        while army1.army and army2.army:
            x = army1.army[0]
            y = army2.army[0]
            # find lancer's second hit in each army
            first = army1.army[0].health
            second = army2.army[0].health
            if fight(x, y):
                first -= army1.army[0].health
                second -= army2.army[0].health
                try:
                    if isinstance(y, Lancer):
                        army1.army[1].health -= first * 0.5
                    if isinstance(x, Lancer):
                        army2.army[1].health -= second * 0.5
                except IndexError:
                    pass
                army2.army.pop(0)
            else:
                first -= army1.army[0].health
                second -= army2.army[0].health
                try:
                    if isinstance(y, Lancer):
                        army1.army[1].health -= first * 0.5
                    if isinstance(x, Lancer):
                        army2.army[1].health -= second * 0.5
                except IndexError:
                    pass
                army1.army.pop(0)
        return True if army1.army else False




        
