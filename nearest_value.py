def nearest_value(values: set, one: int) -> int:
    values = sorted(values)
    if one <= values[0]:
        return values[0]
    if one >= values[-1]:
        return values[-1]
    for i, v in enumerate(values):
        if v == one:
            return one
        if v > one:
            return values[i-1] if abs(values[i-1] - one) <= v - one else v 


