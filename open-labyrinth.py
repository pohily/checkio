MOVE = {"S": (1, 0), "N": (-1, 0), "W": (0, -1), "E": (0, 1)}

def checkio(maze_map):
    
    x = 1
    y = 1
    visited = [(x, y)]
    path = []
    forks = []
    
    while not (x == 10 and y == 10):
        possible = []
        dead_end = True
        #check for amount of possible moves, 
        for move in MOVE:
            i, j = MOVE[move]
            
            if (x+i, y+j) not in visited and maze_map[x+i][y+j] == 0:
                dead_end = False
                possible.append(move)
        
        if dead_end == True:
            # go to previous fork
            tmp, point = forks.pop(-1)
            path = tmp[:]
            x, y = point
            continue
        else:
            if len(possible) > 1:
                # add forks to list
                for pos in possible[1:]:
                    i, j = MOVE[pos]
                    forks.append((path + [pos], (x+i, y+j)))
            
            # make next move
            i, j = MOVE[possible[0]]
            x += i
            y += j
            path.append(possible[0])
            visited.append((x, y))
    return ''.join(path)

        


